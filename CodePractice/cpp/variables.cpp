#include <iostream>

int main() {
    int x; // Declaration of an integer variable 'x'
    x = 5; // Assignment of value 5 to variable 'x'
    int y = 6;

    int sum = x + y; // Declaration and assignment in one line


    std::cout << x << '\n';
    std::cout << y << '\n';
    std::cout << sum << '\n';

    return 0;
}